#include "rtv1.h"

t_v3d	*set_to(t_v3d *a, t_v3d *b)
{
	a->x = b->x;
	a->y = b->y;
	a->z = b->z;
	return (a);
}

void	vector_normalize(t_v3d *v)
{
	double	tmp;

	tmp = sqrt((v->x * v->x) + (v->y * v->y) + (v->z * v->z));
	v->x = v->x / tmp;
	v->y = v->y / tmp;
	v->z = v->z / tmp;
}
